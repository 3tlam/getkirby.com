| Parameter | Type | Required | Description |
| - | - | - | - |
| `code` | `string` | `required` | Needs to be a unique language code (i.e. de, fr, ch, etc.) |
| `name` | `string` | <code></code> | If not passed, the code will be used as name |
| `locale` | `string` | <code></code> | PHP locale string |
| `direction` | `string` | <code></code> | "ltr" or "rtl". "ltr" will be used as default. |
| `url` | `string` | <code></code> | Custom Url path setting for the language. If not set, the language code will be used as Url path. |
