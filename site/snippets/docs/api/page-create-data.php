| Parameter | Type | Required | Description |
| - | - | - | - |
| `slug` | `string` | ✓ | Needs to be unique slug/folder name for the new subpage |
| `title` | `string` | × | If not passed, the slug will be used as title |
| `template` | `string` | × | The name of the page template. If not passed, the default template will be used |
| `content` | `array` | × | Additional content fields. Must be defined as key/value array |
